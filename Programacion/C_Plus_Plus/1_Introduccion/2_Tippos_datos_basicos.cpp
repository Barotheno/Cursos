//Tipos de datos basicos

#include<iostream>

int main(int argc, char const *argv[])
{
	
	//Dato entero:
	int numero = 15;
	//Imprimir variable:
	cout<<numero;
	//Flotante o decimal:
	float decimal = 10.45;
	cout<<decimal;
	//Decimal mas extenso:
	double mayor = 16.3456;
	cout<<mayor;
	//Caracter:
	char letra = 'a';
	cout<<letra;
	
	return 0;
}